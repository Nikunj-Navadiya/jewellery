dotenv.config();
connectDB();

app.use(cors({
  origin: [
    "https://jewellery-w3b3.vercel.app"
  ],
  methods: ["GET", "POST", "PUT", "DELETE", "OPTIONS"],
  allowedHeaders: ["Content-Type", "Authorization"],
  credentials: true
}));

app.options("*", cors());

